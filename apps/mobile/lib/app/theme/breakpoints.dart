/// Responsive breakpoints for adaptive layouts.
///
/// These values define the width thresholds at which the UI
/// adjusts its layout strategy.
abstract final class AppBreakpoints {
  /// 320dp — compact phones (e.g. iPhone SE).
  static const double compactPhone = 320;

  /// 360dp — standard phones.
  static const double standardPhone = 360;

  /// 400dp — large phones.
  static const double largePhone = 400;

  /// 600dp — small tablets.
  static const double smallTablet = 600;

  /// 840dp — large tablets.
  static const double largeTablet = 840;
}
